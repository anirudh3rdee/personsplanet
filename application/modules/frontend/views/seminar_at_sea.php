		      <h2 class="seminar_subheading">Quantum of the Seas Entertainment &amp; Activities</h2>
	        </div>
		    <p class="quantm_activity"><strong>Theater</strong></p>
		    <p>Unlike the line's other mega-ships, Quantum of the Seas no longer has a Broadway show in its Royal Theater. Instead, the entertainment is more like you'd find on an older ships, with shows like "Sequins and Feathers," a retro Las Vegas-style revue that we found fairly appalling in its sexist caricatures. We did enjoy the performances of Funny Bones, a physical comedy duo that somehow managed to be amusing to everyone, regardless of nationality.</p>
		    <p>The ship also has "Sonic Odyssey," created by 2013 "America's Got Talent" contestant William Close. At the heart of the show is the Earth Harp, with strings that stretch from the back of the theater to the stage, as well as a 136-Drum Wall, a Vocal Percussion Jacket and Violin Dress. The result is ethereal music, which, when combined with singers, dancers, aerialists and gymnasts, transports you to another world. It received applause from Western and Asian passengers alike.</p>
		    <p class="quantm_activity"><strong>Daily Fun</strong></p>
		    <p>Quantum of the Seas has a host of daily activities, with options for both Chinese and Western guests. Your Daily Planner will tell you which trivia sessions or events are in what language. If there are only a few English speakers onboard, though, expect the English-language activity to be canceled.</p>
		    <p>The massive SeaPlex, which should be your first stop for recreation, is an impressively sized indoor complex with a large basketball court/roller skating rink/bumper car arena. During the day, the space is used primarily for sports fun like basketball and soccer, but the bumper cars are brought out for a few hours, too. "SeaPods," essentially small rooms for socializing, surround the main court, where gamers can play Xbox with one another or with players around the world.</p>
		    <p>On select days, circus school is open for adults and kids alike. Learn how to juggle and clown around, or dare to hit the wires with trapeze classes. The minimum age for the trapeze school is 6 years old.</p>
		    <p class="quantm_activity"><strong>At Night</strong></p>
		    <p>There is as much to do at night as there is during the day, with the popularity of different venues depending entirely on your cultural tastes. Shows that Westerners might love, such as a virtual reality concert in Two70 by Santa Fe &amp; the Big Horn Band, had Chinese passengers streaming for the door. Likewise, Chinese packed Bolero's to dance along to a live band singing adapted versions of country music and American oldies that seemed incongruous to Americans (Elvis as a cha-cha, anyone?). And international party music -- think the Black-Eyed Peas and Rihanna -- drew the Chinese millennials into the Music Hall for late-night dancing.</p>
		    <p>At night, the SeaPlex transforms into a funky roller skating rink with a floating DJ booth and disco lighting.<br>
		      The bottom line is that everyone can have fun, no matter what their nationality.&nbsp;</p>
		    <p><strong>Casino Royale (Deck 3):</strong>&nbsp;The Casino is a major hotspot on Quantum of the Seas, and you'll find it far more packed than on other Royal Caribbean ships. Gamblers will find slot machines and table games in Casino Royale, hidden down on Deck 3, where it occupies a large section midship. It's decorated in bright red, gold, black and white, with punches of animal print. It's also got some seating areas with plush chairs, sofas and tables near entrances. Beware: It can get quite smoky down there, as it's one of the few places inside the ship you can smoke. It does have a small nonsmoking section, but smoke from the casino tends to seep into that area, as well.</p>
		    <p><strong>Music Hall (Decks 3 and 4):</strong>&nbsp;Outfitted in burgundy red and black, the double-height Music Hall hosts activities during the day, such as Chinese Celebrity Talking Heads, and a variety of bands at night, mostly party and dance music. In front of the stage is a large dance floor, which is always full once the sun goes down. Stairs lead to the upper level, where you can look down on the band and the dancers or find a quieter area for a late-night drink.&nbsp;<br>
		      You'll also find karaoke there during certain times of the day. Or just relax with a beer and a game of pool.</p>
		    <p><strong>Two70 (Decks 5, 6 and 7):</strong>&nbsp;This space at the back of the ship is the piece de resistance of the much-touted advanced technology that Royal has installed onboard Quantum and is, in fact, unique to the world of cruising.&nbsp;<a href="https://www.cruisecritic.com/articles.cfm?ID=2374">Two70</a>&nbsp;has vast windows at the rear, a bar at one side, a stage area with seating in a wide semicircle and more seating on the upper level. During the day, it's part cafe, part activity space. But at night, it transforms into something more akin to Vegas.</p>
		    <p>You can't miss the "wows." The first you'll notice is the transformation of the windows at the back into Vistarama; the floor-to-ceiling glass walls transform into a vast ambient surface more than 100 feet wide and 20 feet tall. There, any scene, real or imagined, is projected in ultra-HD. Vistarama's resolution is nearly twice that of any IMAX cinema and can't be seen anywhere else in the world. When there isn't officially a show going on, the screen serves as a backdrop for the room, showing various animations, such as a Zeppelin balloon, a giant musical fish tank and a large red curtain pulled apart to reveal a stage. It's mind-blowing, but it's just the start.</p>
		    <p>Six robotic screens or "roboscreens," which are fixed to a gantry above and to the right of the stage, really push the innovation envelope. Each is fixed to a robotic arm that -- according to the tech wizards behind them -- is programmed with as many movements as a human arm, calibrated to within a thousandth of a millimeter so they all can move in sync or independently of one another. While they move, they are screening fantastical images onto their ultra-HD screens or interacting with the dancers.</p>
		    <p>During the day, the space is used for a variety of activities, from line dancing lessons to afternoon dance parties.</p>
		    <p>On most nights, the sensory feast for the eyes and ears known as "Starwater" is offered twice. Part new age, part aerial acrobatics and all mind blowing, "Starwater" combines singing, dancing and aerial work with stunning digital imagery on the massive HD screens. Sight lines aren't perfect throughout the space, and cool couch seating during the day becomes natural obstruction at night; Quantum of the Seas has monetized the process and if you want better seats, you'll have to pay an extra $29 for priority seating. Also, if you don't want to be involved in the show, sit in the balcony. If you sit on the lower level, you might end up face to face with a staring singer or dancing with an acrobat.</p>
		    <h2 class="seminar_subheading">Quantum of the Seas Bars and Lounges</h2>
		    <p>Chinese passengers are not big drinkers, and you'll find passengers more apt to be sleeping in some of the bars, such as Vintages or Schooners, than drinking. This changes, of course, when more international passengers are onboard.</p>
		    <p><strong>Boleros (Deck 4):</strong>&nbsp;A happening night spot, Boleros features live music and a dance floor that's usually packed, although the music isn't the Latin style that you'd find in this bar on other Royal Caribbean ships. On our cruise, the main band here played unusual versions of Western songs, to the delight of the Chinese and the confusion of the Americans. &nbsp;</p>
		    <p><strong>Schooner Bar (Deck 5):</strong>&nbsp;Fans of Royal Caribbean might call this the line's signature bar, as it's a favorite gathering spot on virtually every ship. Here it's decidedly less popular with the Chinese, but still draws in Westerners on longer cruises. The Schooner Bar features dark wood, marble and a nautical feel, thanks to decorative rope netting. It's also the ship's piano bar during international sailings; passengers can sit around a grand piano (thanks to the conveniently piano-shaped bar surrounding it), make requests of the piano player and sing along to favorites. On other sailings, a classical music duo plays.</p>
		    <p><strong>Bionic Bar (Deck 5):</strong>&nbsp;Mark this on your list of spots worth visiting once; the Chinese passengers used it primarily as a background for selfies. Most passengers just call it the "robot bar" because it features two&nbsp;<a href="https://www.cruisecritic.com/articles.cfm?ID=2454">robot bartenders</a>&nbsp;that mix drinks via orders from passengers made on tablets. A digital display shows the wait times for drinks, who has ordered what and which ingredients go in each cocktail. It's a lot of fun to watch, but it's really more of a novelty. Wait times for drinks get long quickly, and the space itself doesn't feel like a bar; it is more a walkway that runs along the side of the Esplanade. When the bar is crowded, it's difficult for passengers to cut through. Only passengers 21 or older can order drinks -- SeaPass cards, which are linked to your profile, are required to order -- but kids will get a kick out of the robots at work.</p>
		    <p><strong>Vintages Wine Bar (Deck 5):</strong>&nbsp;At Vintages, passengers can sip their favorite wines while snacking on treats from an abbreviated menu from the adjacent Jamie's Italian. Five appetizers are offered; each is $5. Wine tastings are also held here periodically for $15.</p>
		    <p><strong>Sky Bar and Pool Bar (Deck 14):</strong>&nbsp;The Sky Bar functions as the outdoor pool bar. There, you'll find a small bar with limited seating. The Pool Bar serves the same function for the adjacent covered pool area.</p>
		    <p><strong>North Star Bar (Deck 15):</strong>&nbsp;In good weather, this is a surprisingly popular spot, mostly because it offers a covered bar and has great views of the North Star as it rises and returns to the ship. It also overlooks the lido deck, so you can tune into the action below without actually participating.</p>
		    <h2 class="seminar_subheading">Quantum of the Seas Outside Recreation</h2>
		    <p class="quantm_activity"><strong>Pools</strong></p>
		    <p>There are three main pools, all on Deck 14 -- an outdoor pool, another beneath a retractable roof and a third (tiered) pool in the suite-only Solarium. There are lots of lounge chairs for soaking up the sun, as well as comfy wicker couches in the shade. Near the main pool, you can grab a cool treat from two soft serve ice cream machines. Four hot tubs are available.</p>
		    <p>One nice thing about sailing on Quantum of the Seas is that Chinese passengers are not big sunbathers. This means you can almost always get a deck chair at the outdoor pool, and there's no fear of chair hogs. You'll likely have the hot tubs all to yourself.</p>
		    <p>Toward the aft of the main pool deck area on Deck 14, you'll find the H2O Zone, dedicated to families with young children. It's not completely separate; it's more an adjunct to the main pools, with a wave pool for youngsters (complete with various Madagascar figures, such as the Penguins and Gloria the hippo). Beside that, there's a little splash pool for babies. There are also kid-size deck chairs, which we're pretty sure is a cruise industry first. (How cool is that?)</p>
		    <p>On Quantum, the Solarium space is just for suite passengers; the ship doesn't have any adult-only areas. The space has three tiers of pools spilling over into the next, with forward-facing ocean views, hot tub space and an air of serenity. It's not an area that's widely used by the Chinese.</p>
		    <p>All of the pools on Quantum of the Seas have lifeguards. Complimentary swim vests are available in three sizes for children ages 4 to 12.</p>
		    <p class="quantm_activity"><strong>Recreation</strong></p>
		    <p>Adrenaline junkies should head straight to the back of Deck 16 for&nbsp;<a href="https://www.cruisecritic.com/articles.cfm?ID=2381">iFly by RipCord</a>, a skydiving simulator. Unlike Quantum's sister ships, iFly by RipCord has a charge of $29. You also need reservations for iFly, as it's incredibly popular. Don't be afraid though; it's really a blast to try out, and if you don't like it, you never have to do it again. (Be prepared for a little muscle soreness the next day; you'll find you probably clenched and worked muscles in new ways.) Nearby is the&nbsp;<a href="https://www.cruisecritic.com/articles.cfm?ID=1052">FlowRider surf simulator</a>, as well as a rock climbing wall.</p>
		    <p>One of the unique activities to try out is&nbsp;<a href="https://www.cruisecritic.com/articles.cfm?ID=2459">North Star</a>, a jewel-shaped glass globe that rises 300 feet above sea level and provides 360-degree views from high above the ship. Though North Star is open while the ship is in port, we recommend waiting until it's at sea, as that's the only time crew are permitted to maneuver the pod out over the water. In port, you'll likely be restricted to a straight up-and-back ride. North Star carries a charge on this ship too, of $20.</p>
		    <p>If you cancel iFly or North Star appointments less than 12 hours in advance, you'll be charged a $25 no-show fee.</p>
		    <p class="quantm_activity"><strong>Sun Decks</strong></p>
		    <p>If you want to grab some rays, head up to Deck 15. There, you'll find loungers surrounding the jogging track but little shade.</p>
		    <p>An exclusive sun deck for suite passengers is located on Deck 16, just forward of the SeaPlex's upper level.</p>
		    <h2 class="seminar_subheading">Quantum of the Seas Services</h2>
		    <p>Guest services is in a small, recessed area across from Boleros on Deck 4. For passengers needing help booking restaurants, shows and activities like circus school and iFly, there are several Royal iQ tablets in the area, as well. A crew member is nearby at all times to assist passengers with making their bookings on the tablets.</p>
		    <p>The library/game room is tucked away on the second level of Two70 on Deck 5. (Note that entry to Two70 is from Deck 4.) Bookcases line the walls with fiction and nonfiction titles for adults, plus there's a small children's and young adult section. There's a decent amount of English-language books, as well as Chinese tomes. Passengers are asked to note which books they've borrowed on a sign-out sheet and sign them back in upon return; the books are electronically marked so they will beep if you take them off the ship.</p>
		    <p>Games available include checkers, cribbage and decks of cards. Comfortable armchairs and a handful of tables for playing games are all located in the same area, so it might not be the best place for quiet reading.</p>
		    <p>Wi-Fi through Voom remains among the fastest at sea; we had a strong signal in our cabin and throughout the ship. Packages depend on how many devices you want to connect. Costs begin at $18 per day for one device, $33 per day for two devices and $45 per day for three devices.</p>
		    <p>The Photo Gallery on Deck 5 is completely digital. Just pick a computer station to view, and order your photos. Quantum of the Seas uses face-recognition software so after the ship's photographers snap pics, they're digitally assigned to the right passenger. When you scan your SeaPass card, your photos will come up. You can purchase prints or digital copies. You can also purchase cameras there.</p>
		    <p>The Next Cruise future booking desk is also on Deck 5, near the main elevator bank. Across the way is the Shore Excursions space. Passengers can browse and book excursions on tablets and computer stations; there are no paper brochures.</p>
		    <p>Shopping is a major activity for Chinese passengers on Quantum of the Seas, and the retail shops on the lower and upper Esplanade on Decks 4 and 5 are jumping all the time, day and night. The shopping mania may be confusing, but it stems from the Chinese having large extended families, with members who are all owed a gift from an international trip. To not bring gifts back would incur loss of face, so particularly on a short cruise, the race for shopping can seem intense.</p>
		    <p>High-end boutiques include Regalia Fine Jewelry, Regalia Luxury Watches, Bulgari, Prince &amp; Greene, Sundials, Michael Kors, Coach, Tiffany &amp; Co., Burberry, Bally and Salvatore Ferragamo. Flash sales on the floor are virtually scrums; if you hate crowds, avoid the entire area. &nbsp;</p>
		    <p>Don't miss the duty-free beauty shops, where you can browse cult Korean skincare brands such as Dr. Jart, TonyMoly and Missha, as well as high-priced international beauty lines such as La Prairie. We picked up popular "K-Beauty" items like snail secretion face sheet masks, color correcting powders and BB creams for a fraction of what we'd pay in the United States. An overseas pharmacy where Chinese could buy Japanese products also did brisk business.</p>
		    <p>If you've forgotten your sunscreen, you can stop by Sea Trek by the main pool for a choice of sun-related products.</p>
		    <p>For all things branded Royal Caribbean or Quantum of the Seas, The Shop is the place to spend your dollars. You'll find duty-free liquor and cigarettes next door at Port Merchants.</p>
		    <div>
		      <h2 class="seminar_subheading">Quantum of the Seas Spa &amp; Fitness</h2>
	        </div>
		    <p class="quantm_activity"><strong>Spa</strong></p>
		    <p>The Vitality Spa, located on Deck 15, is a peaceful respite on an otherwise busy ship. Decorated in natural tones of light browns and shades of purple, rose and cranberry, the spa has 22 treatment rooms, including two for couples. Before their chosen treatments, passengers wait in the relaxation room in plush armchairs; free water and tea are available. The spa offers a variety of massages, facials and body treatments, while the salon provides hairstyling, manicures and pedicures. Acupuncture and teeth whitening also are available. An 18 percent auto-gratuity is added to all treatments.</p>
		    <p>The spa's thermal suite is small, with just six heated ceramic loungers, a steam sauna and a dry heat sauna. Only 30 day passes are available on any given day, but these never sell out on primarily Chinese voyages; pricing is $18 per day with a service charge. Weekly passes also are sold. There are no free steam rooms in the changing areas.</p>
		    <p class="quantm_activity"><strong>Fitness</strong></p>
		    <p>Quantum of the Seas has one of the largest and best-equipped fitness facilities at sea. The variety of fitness equipment would make even land-based gyms jealous. Treadmills, ellipticals and stationary bikes are positioned window-side, so great views come standard. In addition to weight machines, free weights are available, including dumbbells (up to 90 pounds) and machines that use weight plates. A small stretching area is available, adjacent to a spacious aerobics studio where classes like TRX ($20 per session) take place, however on our cruise, we didn't see any classes given. Spinning fans will love the cycling studio, designed for indoor cycling ($20 per class).</p>
		    <p>Passengers will find a two-lane jogging track on Deck 15. Wall paintings with inspirational quotes -- a la "it's only a crowd if you're in the back" -- encourage runners. Fewer than three laps make a mile (so you won't feel like you're running in circles), and signs around the deck indicate the starting line, as well as the 5-kilometer and 10-kilometer marks. It also includes a small incline (or decline, depending on your perspective) at the front of the ship, where the track goes by the spa. Your biggest problem using the track for running or walking is dodging the Chinese selfie takers, who cram the top deck for the best photo.</p>
		    <div>
		      <h2 class="seminar_subheading">Quantum of the Seas For Kids</h2>
	        </div>
		    <p>Quantum of the Seas' family offerings are many and varied, as you would expect on a ship this size and from a line as family-oriented as Royal Caribbean. That being said, they aren't heavily used by Chinese passengers, as day care and babysitting outside the family is highly unusual in the culture. Parents prefer to spend time with their children on cruises, and you'll see kids with them at all shows and meals.</p>
		    <p class="quantm_activity"><strong>Kids</strong></p>
		    <p>The kids area, Adventure Ocean, is located across two floors on Decks 11 and 12, toward the front of the ship. Adventure Ocean has six dedicated spaces and is split into four different age categories (ranging from 6 months to 11 years), each with its own room and staff. Complimentary youth programming runs on sea days from 9 a.m. to noon, 2 to 5 p.m. and 7. to 10 p.m. On port days, it goes from 9 a.m. (or 30 minutes before the first shore excursion for early arrivals) until 5 p.m. (lunch included) and 7 to 10 p.m. A chaperoned dinner is on offer some evenings between 5 and 7 p.m. Royal Caribbean features the Late Night Party Zone every evening from 10 p.m. to 2 a.m. for $7 per hour, per child.</p>
		    <p>Parents have to sign their kids in and out. There's a fee of $1 a minute if you don't pick your kids up when the session is over.</p>
		    <p>On Deck 11, you'll find three separate play areas for the littlest cruisers.</p>
		    <p>The Royal Babies &amp; Tots Nursery lets parents drop off little ones, ages 6 to 36 months, for an hourly fee of $6 in the daytime and $8 during the evening (per child). Check to see if the service is being offered on your cruise when you board, as it's rarely in use by the Chinese. Parents can reserve spots for their kids at the beginning of each cruise, and remaining hours are then opened on a first come, first served basis for walk-ins. (Royal Caribbean plans on offering pre-cruise bookings in the future.) All staff in the nursery are specially trained to care for young children, and there are more staff per child there than in the other rooms. The space is given over mainly to soft play and activities; a specific "Sleeping Area" is attached to the rest of the Nursery and furnished with five cribs for the younger children and 10 cots for older children. The Nursery is open 9 a.m. to midnight every day.</p>
		    <p>The Open Play room, adjacent to the Nursery, hosts interactive 45-minute play sessions for Royal Babies (6 to 18 months) and Royal Tots (18 to 36 months). It's also available for little ones and parents to play together throughout the day. This is not a drop-off option. Tots and their parents or caregivers can enjoy music, toys and learning activities developed by early childhood experts at Fisher-Price. The Open Play room has a slide, soft play area, books and a spiral staircase connecting to the upper deck of Adventure Ocean.</p>
		    <p>The Nursery also has six strollers onboard, which can be borrowed on a first-come, first-served basis for free. They have to be returned at the end of the day.</p>
		    <p>The Aquanauts room is for potty-trained 3 to 5 year olds, with a small library, a bank of TV screens for educational shows, and lots of tables and chairs for arts and crafts and board games. Kids don't stay in the room all day; there is a whole program of activities -- such as dressing up, scavenger hunts and doing cool science experiments (with lots of exploding volcanoes) -- that take place around the ship. Parents can opt for pagers to stay in touch with staff.</p>
		    <p>Three other rooms upstairs, on Deck 12, are dedicated to the older kids.</p>
		    <p>The Explorers room is home base for 6 to 8 year olds. This age group gets to learn about space, the oceans and dinosaurs; paint and draw; take part in theater productions and sport competitions; dress up; and go on scavenger hunts. Explorers also have their own line dancing and ice cream parties.</p>
		    <p>Kids ages 9 to 11 also get their own dedicated space in the Voyagers room, but they get out and about more, with backstage ship tours and lots of sports and activities. There are also regular movie nights, including popcorn.</p>
		    <p>In between the Explorers and Voyagers rooms is the Science Lab, which is open to all ages and where the numerous scientific experiments (explosions, usually) are carried out under close supervision.</p>
		    <p>Adventure Ocean is a large, fun, well-thought-out, colorful space that kids will love. The only downside is that there are no windows, so there's no natural light. There are also no dedicated outdoor play areas adjacent to the rooms. No doubt there are good reasons (safety, security), but other lines manage it, so it's a missed opportunity. Still, you'll find a dedicated kids splash and wave pool area called H2O Zone on Deck 14, complete with Madagascar characters like the Penguins and Gloria the hippo.</p>
		    <p>Another missing feature we would like to have seen is a dedicated quiet area for the younger kids who might start nodding off as the evening wears on.</p>
		    <p class="quantm_activity"><strong>Teens</strong></p>
		    <p>At the other end of the ship, on Decks 14 and 15, you'll find the Teen (ages 12 to 17) facilities, which consist of two rooms: Living Room on Deck 14 and Fuel, the teen-only disco, on Deck 15.</p>
		    <p>Teens are allowed to come and go as they please in the Living Room -- no need to sign in or out -- and adult staff presence is limited. If it's a cruise during holiday time with a lot of teens, then staff will divide the group into 12- to 14-year-olds and 15- to 17-year-olds. Note that the cut-off age is 17. The ship has a curfew of 1 a.m. for those under 17.</p>
		    <p>The Living Room is a great space for teens. Chairs and beanbags are scattered about the room, surrounded by foosball, Xboxes and a widescreen TV for movies. A highlight is the cool hangout area by the windows where you can lie back side by side with a pal and watch the TV placed directly in front of you. Activities include age-appropriate scavenger hunts, trivia, game show-style competitions, painting, foosball tournaments and music video creation.</p>
		    <p>Staff are there to guide, suggest and organize, but only if your teens wants to join in. If they just want to hang out and be moody or cool, they can do that, too.</p>
		    <p>Directly above is Fuel, the teens-only disco. On our sailing, however, it was closed every night; dance parties took place in The Living Room instead.</p>
		    <p>There are no teens-only shore excursions, but there are plenty of family shore excursions, which will be marked with a family symbol in the shore excursion guides.</p>
